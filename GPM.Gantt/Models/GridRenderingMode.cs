namespace GPM.Gantt.Models
{
    /// <summary>
    /// Defines the grid rendering modes for the Gantt chart.
    /// </summary>
    public enum GridRenderingMode
    {
        /// <summary>
        /// Render grid as lines.
        /// </summary>
        Lines = 0,
        
        /// <summary>
        /// Render grid as rectangles.
        /// </summary>
        Rectangles = 1,
        
        /// <summary>
        /// No grid rendering.
        /// </summary>
        None = 2
    }
}